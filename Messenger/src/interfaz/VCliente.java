    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        JFileChooser jf = new JFileChooser();
        jf.showDialog(this, "Seleccionar archivo");
        
        if(!jf.getSelectedFile().getPath().isEmpty()){
            historialArchivo = jf.getSelectedFile().getPath();
            if(this.isWin32()){
                historialArchivo = historialArchivo.replace("/", "\\");
            }
            jTextField6.setText(historialArchivo);
            jTextField6.setEditable(false);
            jButton7.setEnabled(false);
            jButton8.setEnabled(true);
            hist = new Historial(historialArchivo);
                    
            historialFrame = new VHistorial(hist);
            historialFrame.setDefaultCloseOperation(JFrame.HIDE_ON_CLOSE);
            historialFrame.setVisible(false);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

